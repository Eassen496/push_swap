#include "../header/ft_printf.h"

int	pourcent_s(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (write(fd, "(null)", 6));
	while (s[i])
	{
		i++;
	}
	return (write(fd, s, i));
}
